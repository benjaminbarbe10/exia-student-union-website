<?php

namespace BDE\ShopBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Articles_categorie
 *
 * @ORM\Table(name="articles_categorie")
 * @ORM\Entity(repositoryClass="BDE\ShopBundle\Repository\Articles_categorieRepository")
 */
class Articles_categorie
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="Name", type="string", length=255)
     */
    private $name;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Articles_categorie
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}

